package kr.co.vo;

public class SearchVO {
	private String year;
	private String semester;
	private String target_grade;
	private String division_cd;
	private String abeekStr;
	private String title;
	public String getYear() {
		return year;
	}
	public void setYear(String year) {
		this.year = year;
	}
	public String getSemester() {
		return semester;
	}
	public void setSemester(String semester) {
		this.semester = semester;
	}
	public String getTarget_grade() {
		return target_grade;
	}
	public void setTarget_grade(String target_grade) {
		this.target_grade = target_grade;
	}
	public String getDivision_cd() {
		return division_cd;
	}
	public void setDivision_cd(String division_cd) {
		this.division_cd = division_cd;
	}
	public String getAbeekStr() {
		return abeekStr;
	}
	public void setAbeekStr(String abeekStr) {
		this.abeekStr = abeekStr;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	

}
